# Logical_Op.py

# Define variables
a = True
b = False

# Logical AND
result_and = a and b
print(f"Logical AND of {a} and {b} is {result_and}")

# Logical OR
result_or = a or b
print(f"Logical OR of {a} and {b} is {result_or}")

# Logical NOT
result_not_a = not a
result_not_b = not b
print(f"Logical NOT of {a} is {result_not_a}")
print(f"Logical NOT of {b} is {result_not_b}")