# Arithmetic Operations in Python

# Addition
a = 10
b = 5
addition = a + b
print(f"Addition: {a} + {b} = {addition}")

# Subtraction
subtraction = a - b
print(f"Subtraction: {a} - {b} = {subtraction}")

# Multiplication
multiplication = a * b
print(f"Multiplication: {a} * {b} = {multiplication}")

# Division
division = a / b
print(f"Division: {a} / {b} = {division}")

# Modulus means the remainder of the division of left operand by the right.cd
modulus = a % b
print(f"Modulus: {a} % {b} = {modulus}")

# Exponentiation
exponentiation = a ** b
print(f"Exponentiation: {a} ** {b} = {exponentiation}")

# Floor Division means the division of operands where the result is the quotient in which the digits after the decimal point are removed.
floor_division = a // b
print(f"Floor Division: {a} // {b} = {floor_division}")